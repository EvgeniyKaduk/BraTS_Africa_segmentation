    "@torch.no_grad()\n",
    "def sliding_window_fast(model, image, patch_size, stride, sw_batch_size=4):\n",
    "    \"\"\"\n",
    "    Ускоренная sliding-window inference.\n",
    "    Без вложенных циклов.\n",
    "    \"\"\"\n",
    "\n",
    "    B, C, D, H, W = image.shape\n",
    "    pd, ph, pw = patch_size\n",
    "    sd, sh, sw = stride\n",
    "    device = image.device\n",
    "\n",
    "    # координаты начала окон\n",
    "    ds = list(range(0, D - pd + 1, sd))\n",
    "    hs = list(range(0, H - ph + 1, sh))\n",
    "    ws = list(range(0, W - pw + 1, sw))\n",
    "\n",
    "    if ds[-1] != D - pd: ds.append(D - pd)\n",
    "    if hs[-1] != H - ph: hs.append(H - ph)\n",
    "    if ws[-1] != W - pw: ws.append(W - pw)\n",
    "\n",
    "    coords = [(d, h, w) for d in ds for h in hs for w in ws]\n",
    "\n",
    "    num_classes = model.out_channels\n",
    "\n",
    "    output = torch.zeros((B, num_classes, D, H, W),\n",
    "                         device=device, dtype=torch.float32)\n",
    "    norm_map = torch.zeros((1, 1, D, H, W),\n",
    "                           device=device, dtype=torch.float32)\n",
    "\n",
    "    weight = get_gaussian_weight(patch_size, device).float()\n",
    "\n",
    "    # === батчевый прогон ===\n",
    "    for i in range(0, len(coords), sw_batch_size):\n",
    "\n",
    "        batch_coords = coords[i:i+sw_batch_size]\n",
    "\n",
    "        patches = torch.cat([\n",
    "            image[..., d:d+pd, h:h+ph, w:w+pw]\n",
    "            for (d, h, w) in batch_coords\n",
    "        ], dim=0)\n",
    "\n",
    "        with torch.amp.autocast(\"cuda\"):\n",
    "            preds = model(patches)\n",
    "\n",
    "        preds = preds.float()\n",
    "\n",
    "        for j, (d, h, w) in enumerate(batch_coords):\n",
    "            output[..., d:d+pd, h:h+ph, w:w+pw] += preds[j:j+1] * weight\n",
    "            norm_map[..., d:d+pd, h:h+ph, w:w+pw] += weight\n",
    "\n",
    "    return output / norm_map.clamp_min(1e-6)"
