    "gaussian_cache = {}\n",
    "\n",
    "def get_gaussian_weight(patch_size, device):\n",
    "    key = (patch_size, device)\n",
    "    if key in gaussian_cache:\n",
    "        return gaussian_cache[key]\n",
    "\n",
    "    dz, dy, dx = patch_size\n",
    "    z = torch.linspace(-1, 1, dz, device=device)\n",
    "    y = torch.linspace(-1, 1, dy, device=device)\n",
    "    x = torch.linspace(-1, 1, dx, device=device)\n",
    "\n",
    "    zz, yy, xx = torch.meshgrid(z, y, x, indexing='ij')\n",
    "    g = torch.exp(-(xx**2 + yy**2 + zz**2) / 0.5)\n",
    "    g = g / g.max()\n",
    "    g = g.half().unsqueeze(0).unsqueeze(0)  # [1,1,D,H,W]\n",
    "\n",
    "    gaussian_cache[key] = g\n",
    "    return g"
